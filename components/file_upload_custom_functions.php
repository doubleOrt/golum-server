function getMimeType($filePath) {

if (!is_file($filePath)) {
return false;
}

$finfo = finfo_open(FILEINFO_MIME_TYPE);
$mime = finfo_file($finfo, $filePath);
finfo_close($finfo);

return $mime;
}

function upload($filePath, $destinationDir, $file_name, array $allowedMimes = array(), $maximum_file_size = 5000000) {

$extMapping = getExtensionToMimeTypeMapping();


if (!is_file($filePath) || !is_dir($destinationDir)) {
return "Sorry, something went wrong :(";
}

if (!($mime = getMimeType($filePath))) {
return "Sorry, something went wrong :(";
}

if (!in_array($mime, $allowedMimes)) {
$invalid_mime_message = "Only ";
for($i = 0; $i < count($allowedMimes); $i++) {
foreach ($extMapping as $extension => $mimeType) {
if ($mimeType == $allowedMimes[$i]) {
if($i == 0) {
$invalid_mime_message .= strtoupper($extension);	
}	
else if($i != count($allowedMimes) - 1) {	
$invalid_mime_message .= ", " . strtoupper($extension);
}
else {
$invalid_mime_message .= " And ". strtoupper($extension);	
}
}
}
}
$invalid_mime_message .= " Are allowed :(";
return $invalid_mime_message;
}

$ext = null;
foreach ($extMapping as $extension => $mimeType) {
if ($mimeType == $mime) {
$ext = $extension;
break;
}
}

if (empty($ext)) {
$ext = pathinfo($filePath, PATHINFO_EXTENSION);
}

if (empty($ext)) {
return "Sorry, something went wrong :(";
}


$fileName = $file_name . '.' . $ext;
$newFilePath = $destinationDir.'/'.$fileName;

if(!rename($filePath, $newFilePath)) {
return "Sorry, something went wrong :(";
}

if(filesize($newFilePath) > $maximum_file_size) {
unlink($newFilePath);	
return "Uploaded files cannot be larger than ". $maximum_file_size/1000000 ."MB";
}

if($mime == "jpg" || $mime == "jpeg") {
$exif = exif_read_data($newFilePath);

if(isset($exif["Orientation"])) {
$orientation = $exif['Orientation'];	
$image_width = imagesx(ImageCreateFromJpeg($newFilePath));
resample($newFilePath,$image_width,$orientation);
}
}

return [true, $ext];
}




function resample($jpgFile, $width, $orientation) {
// Get new dimensions
list($width_orig, $height_orig) = getimagesize($jpgFile);
$height = (int) (($width / $width_orig) * $height_orig);
// Resample
$image_p = imagecreatetruecolor($width, $height);
$image   = imagecreatefromjpeg($jpgFile);
imagecopyresampled($image_p, $image, 0, 0, 0, 0, $width, $height, $width_orig, $height_orig);
// Fix Orientation
switch($orientation) {
case 3:
$image_p = imagerotate($image_p, 180, 0);
break;
case 6:
$image_p = imagerotate($image_p, -90, 0);
break;
case 8:
$image_p = imagerotate($image_p, 90, 0);
break;
}
imagejpeg($image_p,$jpgFile,100);
}


